package main;

public class If_Else {
    public static void main(String[] args){
        int num1=10;
        int num2=9;
        if(num1>num2){
            System.out.println("Число 1 больше числа 2 ");
        }
        else if(num1==num2){
            System.out.println("Числа равны");
        }
        else {
            System.out.println("Число 1 меньше числа 2");
        }
    }
}
